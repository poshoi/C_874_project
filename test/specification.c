/*
1.配置用户信息
git config --global user.name <你的用户名>
git config --global user.email <你的邮箱地址>

2.创建一个新仓库
git init

3.从远程服务器克隆一个仓库
git clone <远程仓库的 url>

4.现实当前的工作目录下的提交文件状态
(类似于 gitkraken 中右方窗口显示的信息)
git status

5.将指定文件Stage(标记为将要被提交的文件)
git add <文件路径>

6.将指定文件Unstage(取消标记为将要被提交的文件)
git reset <文件路径>

7.创建一个提交并提供信息
git commit -m "提交信息"

8.显示提交历史
(类似于Gitkraken中间窗口中显示提交信息)
git log

9.向远程仓库推送 Push
git push

10.从远程仓库拉取 Pull
git pull

*/